const n1= parseFloat(prompt('Insira o primeiro número:'));
const n2= parseFloat(prompt('Insira o segundo número:'));

const soma= n1+n2;
const sub= n1-n2;
const multi= n1*n2;
const div= n1/n2;

alert('A soma de '+n1+' + '+n2+' = '+(soma));
alert('A subtração de '+n1+' - '+n2+' = '+(sub));
alert('A multiplicação de '+n1+' * '+n2+' = '+(multi));
alert('A divisão de '+n1+' / '+n2+' = '+(div));